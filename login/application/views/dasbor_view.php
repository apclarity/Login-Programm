<h1>Sejarah Website</h1>
<p>
Website pertama kali ditemukan oleh Sir Timothy John, Tim Berners-Lee. Pada tahun 1991 website terhubung dengan jaringan.Ttujuan dari 
dibuatnya website pada saat itu yakni untuk mempermudah tukar menukar dan memperbaharui informasi kepada sesama peneliti di tempat 
mereka bekerja. Dengan demikian pengertian website saat itu masih sebatas tukar menukar informasi, bukan pengertian website secara 
terminologi.
</p>